def apply_string_changes(text: str, changes: list) -> str:
    """Применяет список замен (old, new) к строке."""
    for old, new in changes:
        text = text.replace(old, new)
    return text


def process_findings(findings: list) -> tuple[list, list]:
    revised_ids = []
    result = []

    for f in findings:
        fid = f["id"]
        f_copy = dict(f)

        if fid not in REVISIONS:
            # Нет пересмотра
            f_copy["norm_verified"] = True
            f_copy["norm_status"] = "ok"
            f_copy["norm_revision"] = None
            result.append(f_copy)
            continue

        rev = REVISIONS[fid]
        original_norm = f.get("norm", "")
        revised_norm = apply_string_changes(original_norm, rev.get("norm_changes", []))

        # Изменения в description
        original_desc = f.get("description", "")
        revised_desc = original_desc
        for old, new in rev.get("description_changes", []):
            revised_desc = revised_desc.replace(old, new)

        # Изменения в solution
        original_sol = f.get("solution", "")
        revised_sol = original_sol
        for old, new in rev.get("solution_changes", []):
            revised_sol = revised_sol.replace(old, new)

        # Обновляем поля
        f_copy["norm"] = revised_norm
        if revised_desc != original_desc:
            f_copy["description"] = revised_desc
        if revised_sol != original_sol:
            f_copy["solution"] = revised_sol

        # Формируем norm_revision
        norm_revision = {
            "original_norm": original_norm,
            "revised_norm": revised_norm if revised_norm != original_norm else None,
            "original_text": None,
            "revised_text": None,
            "revision_reason": rev["revision_reason"],
        }

        # Если изменился description или solution — фиксируем
        changed_parts = []
        if revised_desc != original_desc:
            changed_parts.append(f"description: {original_desc}")
            norm_revision["original_text"] = f"description: {original_desc}"
            norm_revision["revised_text"] = f"description: {revised_desc}"
        if revised_sol != original_sol:
            sol_note = f"solution: {original_sol}"
            if norm_revision["original_text"]:
                norm_revision["original_text"] += f"\n\nsolution: {original_sol}"
                norm_revision["revised_text"] += f"\n\nsolution: {revised_sol}"
            else:
                norm_revision["original_text"] = f"solution: {original_sol}"
                norm_revision["revised_text"] = f"solution: {revised_sol}"

        f_copy["norm_verified"] = True
        f_copy["norm_status"] = rev["norm_status"]
        f_copy["norm_revision"] = norm_revision
        result.append(f_copy)
        revised_ids.append(fid)

    return result, revised_ids


def main():
    # Читаем входные данные
    with open(INPUT_FILE, encoding="utf-8") as f:
        data = json.load(f)

    with open(CHECKS_FILE, encoding="utf-8") as f:
        checks = json.load(f)

    # Обрабатываем замечания
    processed_findings, revised_ids = process_findings(data["findings"])

    # Подсчёт статистики по нормам
    norms_ok = sum(1 for c in checks["checks"] if not c.get("needs_revision", False))
    norms_revised = sum(1 for c in checks["checks"] if c.get("needs_revision", False))

    # Формируем мета
    meta = dict(data["meta"])
    meta["norm_verification"] = {
        "verified_at": datetime.now().strftime("%Y-%m-%dT%H:%M:%S"),
        "total_norms_checked": checks["meta"]["total_checked"],
        "norms_ok": norms_ok,
        "norms_revised": norms_revised,
        "findings_revised": sorted(revised_ids, key=lambda x: int(x.split("-")[1])),
    }

    # Формируем выходной файл
    output = {
        "meta": meta,
        "findings": processed_findings,
        "quick_index": data.get("quick_index", {}),
    }

    # Сохраняем
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    # Отчёт
    ok_count = sum(1 for f in processed_findings if f["norm_status"] == "ok")
    revised_count = sum(1 for f in processed_findings if f["norm_status"] == "revised")
    warning_count = sum(1 for f in processed_findings if f["norm_status"] == "warning")

    print(f"✓ Записан: {OUTPUT_FILE}")
    print(f"\n── Статистика ──────────────────────────────────────")
    print(f"  Всего замечаний:    {len(processed_findings)}")
    print(f"  norm_status=ok:     {ok_count}")
    print(f"  norm_status=revised:{revised_count}")
    print(f"  norm_status=warning:{warning_count}")
    print(f"\n── Пересмотренные замечания ({revised_count + warning_count}) ────────")
    for f in processed_findings:
        if f["norm_status"] != "ok":
            print(f"  [{f['norm_status'].upper():8}] {f['id']}: {f['problem'][:70]}...")


if __name__ == "__main__":
    main()
