# Findings to SKIP (leave flag as-is):
# F-008, F-015, F-016, F-017, F-020, F-022, F-047, F-048, F-051, F-053, F-069, F-093

FLAG_PATTERN = re.compile(r'^\[Пункт нормы (.+?) требует ручной сверки\]\s*')


def strip_flag(description):
    """Remove the flag prefix from description, return (stripped_text, flag_content)."""
    m = FLAG_PATTERN.match(description)
    if m:
        return description[m.end():], m.group(1)
    return description, None


def apply_fixes(data):
    fixed = 0
    skipped_no_flag = 0
    skipped_no_fix = 0

    for finding in data["findings"]:
        fid = finding["id"]
        desc = finding.get("description", "")

        # Check if this finding has the flag
        m = FLAG_PATTERN.match(desc)
        if not m:
            continue  # no flag, skip

        if fid not in FIXES:
            skipped_no_fix += 1
            print(f"  SKIP (no fix rule): {fid}")
            continue

        fix = FIXES[fid]
        flag_content = m.group(1)

        # Strip flag prefix from description, add suffix
        stripped_desc = desc[m.end():]
        # Also handle double-flagged descriptions (some have two flags)
        m2 = FLAG_PATTERN.match(stripped_desc)
        if m2:
            stripped_desc = stripped_desc[m2.end():]

        new_para_label = fix["new_para_label"]
        new_desc = stripped_desc.rstrip() + f" [norm_requote: {new_para_label}]"

        # Update fields
        old_norm = finding.get("norm", "")
        old_norm_quote = finding.get("norm_quote", "")

        finding["description"] = new_desc
        finding["norm"] = fix["new_norm"]
        finding["norm_quote"] = fix["new_norm_quote"]
        # Update norm_quote_status to reflect we now have real text
        finding["norm_quote_status"] = "exact"

        print(f"  FIXED: {fid}")
        print(f"    norm: {old_norm!r}")
        print(f"      → {fix['new_norm']!r}")
        print(f"    norm_quote: {(old_norm_quote or '')[:60]!r}...")
        print(f"      → {fix['new_norm_quote'][:60]!r}...")
        fixed += 1

    return fixed, skipped_no_fix


def main():
    import os
    os.chdir("/home/coder/projects/PDF-proverka")

    print(f"Loading: {PATH}")
    with open(PATH, "r", encoding="utf-8") as f:
        data = json.load(f)

    print(f"Total findings: {len(data['findings'])}")
    print()

    fixed, skipped = apply_fixes(data)

    print()
    print(f"Results: fixed={fixed}, skipped_no_fix_rule={skipped}")

    # Update meta
    data["meta"]["mcp_fix_applied"] = True
    prev = data["meta"].get("mcp_fix_stats", {})
    data["meta"]["mcp_fix_stats"] = {
        "fixed": prev.get("fixed", 0) + fixed,
        "cleared_flag": fixed,
        "unchanged": prev.get("unchanged", 0),
    }

    with open(PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Saved: {PATH}")


if __name__ == "__main__":
    main()
