revised_ids = []

for finding in result['findings']:
    fid = finding['id']
    if fid in revisions:
        rev = revisions[fid]
        norm_status_override = rev.get('norm_status_override')

        # Determine norm_status
        if norm_status_override:
            norm_status = norm_status_override
        elif rev['revised_norm'] is not None:
            norm_status = 'revised'
            revised_ids.append(fid)
        else:
            norm_status = 'warning'

        if norm_status == 'revised':
            revised_ids.append(fid)

        # Apply norm field update
        if rev.get('norm_field'):
            finding['norm'] = rev['norm_field']

        # Apply description update
        if rev.get('revised_text'):
            finding['description'] = rev['revised_text']

        # Apply solution update
        if rev.get('solution_field'):
            finding['solution'] = rev['solution_field']

        finding['norm_verified'] = True
        finding['norm_status'] = norm_status
        finding['norm_revision'] = {
            'original_norm': rev['original_norm'],
            'revised_norm': rev['revised_norm'],
            'original_text': rev['original_text'],
            'revised_text': rev['revised_text'],
            'revision_reason': rev['revision_reason'],
        }
    else:
        finding['norm_verified'] = True
        finding['norm_status'] = 'ok'
        finding['norm_revision'] = None

# Remove duplicates from revised_ids
revised_ids = list(dict.fromkeys(revised_ids))

# Update meta
norms_ok = sum(1 for f in result['findings'] if f.get('norm_status') == 'ok')
norms_revised = len(revised_ids)
norms_warning = sum(1 for f in result['findings'] if f.get('norm_status') == 'warning')

result['meta']['norm_verification'] = {
    'verified_at': '2026-03-21T23:30:00+03:00',
    'total_norms_checked': 11,
    'norms_ok': norms_ok,
    'norms_revised': norms_revised,
    'norms_warning': norms_warning,
    'findings_revised': revised_ids,
}

