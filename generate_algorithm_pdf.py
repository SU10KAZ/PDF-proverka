#!/usr/bin/env python3
"""
Генерация PDF-документа: «Алгоритм проверки проектной документации».
Описывает весь конвейер простыми словами, без упоминания конкретных файлов/скриптов.
"""
from fpdf import FPDF
from pathlib import Path
import datetime


# ═══════════════════════════════════════════════════════════════════════════════
# Конфигурация
# ═══════════════════════════════════════════════════════════════════════════════

FONT_DIR = Path("C:/Windows/Fonts")
OUTPUT = Path(__file__).parent / "Алгоритм проверки документации.pdf"

# Цвета (R, G, B)
C_DARK = (30, 36, 50)           # основной текст
C_ACCENT = (0, 102, 179)        # заголовки, акценты
C_LIGHT_BG = (240, 245, 250)    # фон блоков
C_STAGE_BG = (230, 240, 252)    # фон этапов
C_WHITE = (255, 255, 255)
C_BORDER = (180, 200, 220)
C_GREEN = (34, 139, 34)
C_ORANGE = (210, 120, 20)
C_RED = (190, 40, 40)
C_GRAY = (120, 130, 140)
C_ARROW = (0, 80, 150)


class AlgorithmPDF(FPDF):
    def __init__(self):
        super().__init__(orientation="P", unit="mm", format="A4")
        self.set_auto_page_break(auto=True, margin=25)

        # Регистрация шрифтов
        self.add_font("Arial", "", str(FONT_DIR / "arial.ttf"))
        self.add_font("Arial", "B", str(FONT_DIR / "arialbd.ttf"))
        self.add_font("Arial", "I", str(FONT_DIR / "ariali.ttf"))

    def header(self):
        if self.page_no() == 1:
            return  # титульная страница без шапки
        self.set_font("Arial", "I", 8)
        self.set_text_color(*C_GRAY)
        self.cell(0, 6, "Алгоритм проверки проектной документации", align="L")
        self.cell(0, 6, f"стр. {self.page_no()}", align="R", new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*C_BORDER)
        self.line(self.l_margin, self.get_y(), self.w - self.r_margin, self.get_y())
        self.ln(4)

    def footer(self):
        self.set_y(-15)
        self.set_font("Arial", "I", 7)
        self.set_text_color(*C_GRAY)
        date_str = datetime.date.today().strftime("%d.%m.%Y")
        self.cell(0, 8, f"Сформировано: {date_str}", align="R")

    # ── Утилиты ──────────────────────────────────────────────────────────

    def _check_space(self, needed_mm: float):
        """Если места на странице не хватает — новая страница."""
        if self.get_y() + needed_mm > self.h - self.b_margin:
            self.add_page()

    def section_title(self, text: str, num: str = ""):
        """Заголовок раздела."""
        self._check_space(20)
        self.ln(6)
        self.set_font("Arial", "B", 16)
        self.set_text_color(*C_ACCENT)
        if num:
            self.cell(0, 10, f"{num}. {text}", new_x="LMARGIN", new_y="NEXT")
        else:
            self.cell(0, 10, text, new_x="LMARGIN", new_y="NEXT")
        # Линия под заголовком
        y = self.get_y()
        self.set_draw_color(*C_ACCENT)
        self.set_line_width(0.6)
        self.line(self.l_margin, y, self.l_margin + 60, y)
        self.set_line_width(0.2)
        self.ln(5)

    def sub_title(self, text: str):
        """Подзаголовок."""
        self._check_space(14)
        self.ln(3)
        self.set_font("Arial", "B", 12)
        self.set_text_color(*C_DARK)
        self.cell(0, 8, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def body_text(self, text: str):
        """Основной текст."""
        self.set_font("Arial", "", 10.5)
        self.set_text_color(*C_DARK)
        self.multi_cell(0, 6, text)
        self.ln(2)

    def bullet_list(self, items: list[str]):
        """Маркированный список."""
        self.set_font("Arial", "", 10.5)
        self.set_text_color(*C_DARK)
        for item in items:
            self._check_space(8)
            x0 = self.get_x()
            self.cell(8, 6, "  \u2022  ", new_x="END")
            self.multi_cell(0, 6, item)
            self.ln(1)
        self.ln(2)

    def stage_block(self, number: str, title: str, description: str,
                    details: list[str] = None, icon: str = ""):
        """Блок этапа конвейера — визуально выделенный."""
        # Оценка высоты
        needed = 30 + (len(details) * 7 if details else 0) + 10
        self._check_space(min(needed, 80))

        x0 = self.l_margin
        w = self.w - self.l_margin - self.r_margin
        y0 = self.get_y()

        # Фон этапа
        self.set_fill_color(*C_STAGE_BG)
        self.set_draw_color(*C_ACCENT)
        self.set_line_width(0.4)

        # Рисуем рамку (высоту определим позже)
        # Сначала содержимое

        # Номер + заголовок
        self.set_xy(x0 + 2, y0 + 3)
        self.set_font("Arial", "B", 22)
        self.set_text_color(*C_ACCENT)
        num_w = self.get_string_width(number) + 4
        self.cell(num_w, 12, number, new_x="END")

        self.set_font("Arial", "B", 13)
        self.set_text_color(*C_DARK)
        self.cell(0, 12, f"  {icon}  {title}" if icon else f"  {title}",
                  new_x="LMARGIN", new_y="NEXT")

        # Описание
        self.set_x(x0 + 4)
        self.set_font("Arial", "", 10)
        self.set_text_color(60, 70, 80)
        self.multi_cell(w - 8, 5.5, description)
        self.ln(1)

        # Детали
        if details:
            for d in details:
                self._check_space(7)
                self.set_x(x0 + 10)
                self.set_font("Arial", "", 9.5)
                self.set_text_color(70, 80, 90)
                self.cell(5, 5.5, "- ")
                self.multi_cell(w - 20, 5.5, d)

        y1 = self.get_y() + 3

        # Рамка
        self.rect(x0, y0, w, y1 - y0, style="D")
        # Левая цветная полоса
        self.set_fill_color(*C_ACCENT)
        self.rect(x0, y0, 1.5, y1 - y0, style="F")

        self.set_y(y1)
        self.ln(2)

    def arrow_down(self):
        """Стрелка вниз между этапами."""
        self._check_space(12)
        cx = self.w / 2
        y = self.get_y() + 2
        self.set_draw_color(*C_ARROW)
        self.set_fill_color(*C_ARROW)
        self.set_line_width(0.8)
        self.line(cx, y, cx, y + 6)
        # Треугольник
        self.set_line_width(0.3)
        x1, y1 = cx - 2.5, y + 5
        x2, y2 = cx + 2.5, y + 5
        x3, y3 = cx, y + 9
        # Рисуем стрелку линиями
        self.line(x1, y1, x3, y3)
        self.line(x2, y2, x3, y3)
        self.set_y(y + 11)

    def info_box(self, title: str, text: str, color: tuple = C_GREEN):
        """Информационный блок с цветной рамкой."""
        self._check_space(25)
        x0 = self.l_margin + 5
        w = self.w - self.l_margin - self.r_margin - 10
        y0 = self.get_y()

        self.set_xy(x0 + 4, y0 + 2)
        self.set_font("Arial", "B", 10)
        self.set_text_color(*color)
        self.cell(0, 6, title, new_x="LMARGIN", new_y="NEXT")

        self.set_x(x0 + 4)
        self.set_font("Arial", "", 9.5)
        self.set_text_color(*C_DARK)
        self.multi_cell(w - 8, 5.5, text)

        y1 = self.get_y() + 2
        self.set_draw_color(*color)
        self.set_line_width(0.5)
        self.rect(x0, y0, w, y1 - y0, style="D")
        self.set_fill_color(*color)
        self.rect(x0, y0, 1.2, y1 - y0, style="F")
        self.set_y(y1 + 3)

    def key_value_table(self, rows: list[tuple[str, str]]):
        """Таблица ключ-значение."""
        self._check_space(len(rows) * 8 + 5)
        x0 = self.l_margin + 3
        col1_w = 70
        col2_w = self.w - self.l_margin - self.r_margin - col1_w - 6

        for i, (key, val) in enumerate(rows):
            y = self.get_y()
            if i % 2 == 0:
                self.set_fill_color(*C_LIGHT_BG)
                self.rect(x0, y, col1_w + col2_w, 7, style="F")

            self.set_xy(x0 + 2, y)
            self.set_font("Arial", "B", 9.5)
            self.set_text_color(*C_DARK)
            self.cell(col1_w - 2, 7, key)

            self.set_font("Arial", "", 9.5)
            self.set_text_color(60, 70, 80)
            self.cell(col2_w, 7, val, new_x="LMARGIN", new_y="NEXT")

        self.ln(4)


